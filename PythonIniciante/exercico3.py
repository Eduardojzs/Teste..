primeiro_valor = input('Digite Primeiro Valor: ')
segundo_valor = input('Digite Segundo Valor: ')



if primeiro_valor > segundo_valor:
    print(f'Primeiro Número {primeiro_valor} é maior que Segundo {segundo_valor}')

elif segundo_valor > primeiro_valor:
    print(f'Segundo Número {segundo_valor} é maior que Que o Primeiro {primeiro_valor}')
else:
    print('Numeros são iguais')